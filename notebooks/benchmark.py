@app.cell
def _(exception, mo, result):
    import ast, tempfile, shutil, subprocess, sys

    def cleanup(message: str):
        if r"```python" in message:
            message = message.split(r"```python")[-1]
        message = message.replace(r"```", "")
        try:
            tree = ast.parse(message)
            imports, funcs = [], []
            # original code uses ast.walk; it can duplicate functions if they are nested.
            # tree.body only extracts the top level imports and function defs which we assume was the intended behavior.
            for node in tree.body:
                if isinstance(node, (ast.Import, ast.ImportFrom)):
                    imports.append(ast.unparse(node))
                elif isinstance(node, ast.FunctionDef):
                    funcs.append(ast.unparse(node))
            return "\n".join(imports) + "\n\n" + "\n\n".join(funcs)
        except Exception:
            print(f'Code is not valid: {exception}')
            return message

    mo.md(f'''
    ```python
    {cleanup(result.choices[0].message.content)}
    ```
    ''')
    return


